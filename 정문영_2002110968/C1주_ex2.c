#include <stdio.h> //stdio.h라는 헤더파일을 포함시킨다

int main(void)
{
    //10과 20을 더한 값을 출력하고 줄바꿈시킨다
    printf("10 + 20 = %d \n", 10 + 20);
    //56과 23을 뺀 값을 출력하고 줄바꿈시킨다
    printf("56 - 23 = %d \n", 56 - 23);
    //10과 20을 곱한 값을 출력하고 줄바꿈시킨다
    printf("10 * 20 = %d \n", 10 * 20);
    //100과 20을 나눈 값을 출력하고 줄바꿈시킨다
    printf("100 / 20 = %d \n", 100 / 20);

    return 0; //값을 반환하고 프로그램 종료
}